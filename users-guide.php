          <h1 class="text-center">Dashboard Community Calendar Made Easy!</h1>
          <p class="text-center">Prepared by: Madeleine Gefke, Ananya Gupta</p>
          <h4>Process</h4>
          <p><b>Step 1:</b> Go to - <a href="http://environmentaldashboard.org/calendar/">http://environmentaldashboard.org/calendar/</a></p>
          <p><b>Step 2:</b> Click “Submit an Event” button at the top far right of page.</p>
          <p><b>Step 3:</b> Fill out quick form about your event (date, time, location, description, etc...)</p>
          <p><b>Step 4:</b> Submit!! You should receive email confirmation indicating that the event was successfully submitted.</p>
          <p><b>Step 5:</b> Events that are submitted need to be approved by a calendar administrator.  Once approved, your event will appear on both the online calendar (with the detailed information you provided) and on digital screens around town (as a slide with more limited information)!</p>
          <h4>Volunteer Opportunities</h4>
          <p>We emphasize volunteer events by adding a specific volunteer icon to the digital screen displays.  Posting works the same as for other events, except that you should select the “volunteer opportunity” option for the event type.</p>
          <h4>Posting Policy and Guidlines</h4>
          <p>This is an <b>Oberlin Community Calendar</b>.  The digital signs only exist in the city of Oberlin; the target audience for all posted events should be the diverse members of the Oberlin community.</p>
          <p>Dos:</p>
          <ol>
            <li>Post events of potential interest to the entire Oberlin community on multiple screens (but see note on schools under don’ts).  Events that occur outside of the immediate Oberlin vicinity should be posted on multiple screens only if they are likely to be of particular interest to residents of Oberlin who regularly experience the digital signs.  Example: the Oberlin Choristers are mostly Oberlin children -- posting a Choristers concert that occurs in Lorain therefore makes sense. Oberlin Community Services attracts clients from across the county.  They might therefore choose to post county-wide events on the screen hosted in their own facility.</li>
            <li>Post events of restricted interest to particular organization’s screen only.  Examples: Slow train introduces a new coffee flavor (Slow Train screen only); Langston school newspaper club meeting (Langston only); High School Athletic event (HS only)</li>
          </ol>
          <p>Don'ts:</p>
          <ol>
            <li>Post events that are specifically related to religious worship or are of primary interest to a particular religion.  (However, events that meet the other criteria on this list and are merely hosted in a church, synagogue or other religious facility are welcome)</li>
            <li>Post events that are direct political action (e.g. protests against xyz).  Environmental Dashboard is a non-partisan organization and wishes to appear so.  However, public discussions of political realities and civic engagement are encouraged.</li>
            <li>Post advertisements for products or services provided by businesses except on screens housed within those businesses.  It is, however, fine for businesses to post events related to visual or performing arts that take place in businesses but are open to the public, even if there is a cost.</li>
            <li>Upload posters that contain text information about events as photographs in the upload section. All info about the event should be included in the text boxes.</li>
            <li>Post to signs in the schools unless you have permission to do so from the Oberlin public schools.</li>
          </ol>
